    [TestCase(20150818, 20240322, 0)]
    [TestCase(20150818, 20240322, 10)]
    [TestCase(20150818, 20240323, 0)]
    public void NoArbitrage(int trsEffective, int trsMaturity, int minute)
    {
      var ibond = GetInflationBond();

      Dt effective = new Dt(trsEffective),
        maturity = Dt.Roll(Date(trsMaturity, minute),
          ibond.BDConvention, ibond.Calendar);

      // Calculate the arbitrage free initial bond price
      Dt asOf = effective;
      var discountCurve = new DiscountCurve(asOf, 0.01);
      var inflationCurve = new InflationCurve(asOf, ibond.BaseInflation,
        new DiscountCurve(asOf, 0.02), discountCurve, null)
      {
        Calibrator = new InflationCurveFitCalibrator(asOf, asOf, discountCurve,
          (InflationIndex) ibond.ReferenceIndex, null),
        ReferenceIndex = ibond.ReferenceIndex
      };

      var initPrice = CalculateBondPrice(asOf,
        ibond, discountCurve, inflationCurve);
      var endPrice = CalculateBondPrice(maturity,
        ibond, discountCurve, inflationCurve, true);
      var initRatio = CalculateIndexRatio(asOf, ibond, inflationCurve);
      var endRatio = CalculateIndexRatio(maturity, ibond, inflationCurve);
      var df = discountCurve.DiscountFactor(asOf, maturity);

      var returnLeg = AssetReturnLeg.Create(
        ibond, effective, maturity,
        ibond.Ccy, initPrice, ibond.Calendar, ibond.BDConvention);

      // This is the expected pv of the Bond Return Leg.
      var expect = (initRatio - df)*initPrice + (1 - endRatio)*df*endPrice;

      // Create a pricer for the Bond Return Leg and calculate Pv
      var pricer = new InflationBondReturnLegPricer(returnLeg,
        asOf, asOf, discountCurve, inflationCurve, null, null)
      {Notional = initPrice, WithDeflater = true};
      var pv = pricer.ProductPv();

      // Do we get the expected results?
      pv.IsExpected(To.Match(expect).Within(1E-15));
      return;
    }

    [Test]
    public void CleanToDirtyPrices()
    {
      var bond = GetInflationBond();

      Dt effective = new Dt(20160210),
        maturity = Dt.Roll(bond.Maturity,
          bond.BDConvention, bond.Calendar);

      // Set pricing date 20 days after the effective
      Dt asOf = Dt.Roll(effective, bond.BDConvention, bond.Calendar);

      // Create the return leg pricer
      var returnLeg = AssetReturnLeg.Create(bond, effective, maturity,
        bond.Ccy, 1.06, bond.Calendar, bond.BDConvention);

      // Create a return leg pricer using the clean price observations
      var inflationIndex = new InflationIndex("GBPRPI_Index", Currency.GBP,
        DayCount.ActualActualBond, Calendar.LNB, BDConvention.Following,
        Frequency.Monthly, Tenor.Empty)
      {
        HistoricalObservations = historicalRpis
      };
      var dumyDiscount = new DiscountCurve(asOf, 0.0);
      var inflationCurve = new InflationCurve(asOf, 1.0, dumyDiscount,
        dumyDiscount, null);
      inflationCurve.Calibrator = new InflationCurveFitCalibrator(asOf,asOf,
        dumyDiscount,inflationIndex,null);
      var pricer = new InflationBondReturnLegPricer(returnLeg, asOf, asOf,
        null, inflationCurve, null, _flatPriceIndex);

      // Get the price calculator from it
      var calculator = pricer.GetPriceCalculator();

      // Now test if the price calculator returns the full prices
      var fullPrices = _fullPriceIndex.HistoricalObservations;
      foreach (var price in fullPrices)
      {
        var expect = price.Value;
        var actual = calculator.GetPrice(price.Date).Value;
        actual.IsExpected(To.Match(expect).Within(6E-6));
      }

    }

    [Test]
    public void UnifiedCreation()
    {
      var ibond = GetInflationBond();
      Dt effective = new Dt(20150618),
        maturity = Dt.Roll(new Dt(20160907),
          ibond.BDConvention, ibond.Calendar);
      var returnLeg = AssetReturnLeg.Make(ibond, effective, maturity,
        ibond.Ccy, double.NaN, ibond.Calendar);
      returnLeg.IsExpected(To.Be.InstanceOf<AssetReturnLeg<InflationBond>>());

      // Calculate the arbitrage free initial bond price
      Dt asOf = effective;
      var discountCurve = new DiscountCurve(asOf, 0.02);
      var discountCurveForProject = new DiscountCurve(asOf, 0.01);
      var inflationCurve = new InflationCurve(asOf, ibond.BaseInflation,
        new DiscountCurve(asOf, 0.02), discountCurve, null)
      {
        Calibrator = new InflationCurveFitCalibrator(asOf, asOf, discountCurve,
          (InflationIndex) ibond.ReferenceIndex, null),
        ReferenceIndex = ibond.ReferenceIndex
      };
      var pricer = returnLeg.CreatePricer(asOf, asOf, discountCurve,
        new CalibratedCurve[] {inflationCurve, discountCurveForProject }, null);
      pricer.IsExpected(To.Be.InstanceOf<InflationBondReturnLegPricer>());

      var ipricer = (InflationBondReturnLegPricer)pricer;
      ipricer.DiscountCurve.IsExpected(To.Be.SameAs(discountCurve));
      ipricer.DiscountCurveForPriceProjection.IsExpected(
        To.Be.SameAs(discountCurveForProject));

    }

    private static Dt Date(int date, int minutes)
    {
      if (minutes > 0)
      {
        return new Dt(date%100, date%10000/100,
          date/10000, minutes/100, minutes%100, 0);
      }
      return new Dt(date);
    }

    private static double CalculateIndexRatio(Dt date,
      InflationBond ibond, InflationCurve inflationCurve)
    {
      var asOf = inflationCurve.AsOf;
      var bondPricer = new InflationBondPricer(ibond, asOf, asOf,
        1.0, null, inflationCurve.InflationIndex,
        inflationCurve, null, null);
      return bondPricer.IndexRatio(date);
    }

    private static double CalculateBondPrice(Dt date, InflationBond ibond,
      DiscountCurve discountCurve, InflationCurve inflationCurve,
      bool includeSettlePayment = false)
    {
      var asOf = inflationCurve.AsOf;
      var pricer = new InflationBondPricer(ibond, asOf, asOf,
        1.0, discountCurve, inflationCurve.InflationIndex,
        inflationCurve, null, null)
      {
        DiscountingAccrued = true
      };
      var ps = pricer.GetPaymentSchedule();
      if (ps == null) return 0;
      return ps.Pv(date, date, discountCurve, null,
        includeSettlePayment || pricer.IncludeSettlePayments,
        pricer.DiscountingAccrued)/pricer.IndexRatio(date);
    }

    private static InflationBond GetInflationBond()
    {
      Dt effective = new Dt(20121012), maturity = new Dt(20240322);
      BondType type = BondType.UKGilt;
      Currency ccy = Currency.GBP;
      DayCount dayCount = DayCount.ActualActualBond;
      Calendar calendar = Calendar.LNB;
      Frequency freq = Frequency.SemiAnnual;
      BDConvention roll = BDConvention.Following;
      double coupon = 0.00125;
      return new InflationBond(effective, maturity, ccy, type,
        coupon, dayCount, CycleRule.None, freq, roll, calendar,
        GetInflationIndex(), 242.41935, Tenor.Parse("3M"))
      {
        Description = "0 1/8% Index-linked Treasury Gilt 2024"
      };
    }

    private static InflationIndex GetInflationIndex()
    {
      return new InflationIndex("RPIGBP_INDEX", Currency.GBP,
        DayCount.ActualActualBond, Calendar.LNB,
        BDConvention.Modified, Frequency.Monthly, Tenor.Empty)
      {
        HistoricalObservations = historicalRpis
      };
    }

